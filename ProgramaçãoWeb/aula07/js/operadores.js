/*

    Vamos Entender Operadores:
-> Os operadores JavaScript são usados para atribuir valores, comparar valores, executar operações aritméticas e muito mais.

São os sinais que usamos: +, -, *, /, %, =, +=, -=, ++, --, &&, ||, etc...

São separados em 6 "categorias":

1) Operadores Aritméticos (Matemáticos)
2) Operadores de Atribuição
3) Operadores de Sequência
4) Operadores de Comparação
5) Operador Condicional (Ternário)
6) Operadores Lógicos
*/

/*
1) Operadores Aritméticos (Matemáticos)

var valor1, valor2, total;
valor1 = 5;
valor2 = 2;

total = valor1 (+, -, *, /) valor2;
*/

/*

2) Operadores de Atribuição

var valor1, valor2, total;
valor1 = 5;
valor2 = 2;

valor1 = valor1 + valor2;
valor1 = valor1 - valor2;
valor1 = valor1 * valor2;
valor1 = valor1 / valor2;

volor1 += valor2;
volor1 -= valor2;
volor1 *= valor2;
volor1 /= valor2;
*/

/*

3) Operadores de Sequência

var valor1, valor2, total;
valor1 = "Tiago";
valor2 = "Da Rocha";

total = valor1 + valor2;
*/

/*

4) Operadores de Comparação

var valor1, valor2, total;
valor1 = 8;
valor2 = 10;

total = (valor1 == valor2); - true/false
total = (valor1 != valor2); - true/false
total = (valor1 === valor2); - true/false
total = (valor1 !== valor2); - true/false
total = (valor1 < valor2); - true/false
total = (valor1 > valor2); - true/false
total = (valor1 <= valor2); - true/false
total = (valor1 >= valor2); - true/false
*/

/*

5) Operador Condicional (Ternário)

var idade, eleitor;
idade = 16;
eleitor = (idade < 16) ? "Não vota ainda"(true) : "Já pode votar"(false);
*/

/*

6) Operadores Lógicos

var idade, resultado;
idade = 45;

resultado = (idade < 50 && idade > 30); - true/false
resultado = (idade == 50 || idade == 45); - true/false
resultado = !(idade != 45); - true/false
*/